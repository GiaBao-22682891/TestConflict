public class HungLon {
    private String duMa;
    private int duMe;

    public HungLon(String duMa, int duMe) {
        this.duMa = duMa;
        this.duMe = duMe;
    }

    public String getDuMa() {
        return duMa;
    }

    public void setDuMa(String duMa) {
        this.duMa = duMa;
    }

    public int getDuMe() {
        return duMe;
    }

    public void setDuMe(int duMe) {
        this.duMe = duMe;
    }
}
